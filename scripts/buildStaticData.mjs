// ========== 生成数据 ==========
const result = {};
const STYLES = ['hardcore', 'chill', 'resort', 'outdoor'];

for (const [destId, data] of Object.entries(DEST_DATA)) {
  for (const style of STYLES) {
    const key = `${destId}-${style}`;
    const activities = data[style];
    if (!activities || activities.length < 12) {
      console.warn(`⚠️ ${key} 活动数量不足 (${activities?.length || 0})`);
      continue;
    }

    const days = [];

    // Day 1: 到达
    days.push({
      day: 1,
      title: `BOOM! 空降${data.city}`,
      desc: ARRIVALS[style](data.city, data.arrive),
      iconName: 'Plane'
    });

    // Day 2-13: 活动
    for (let i = 0; i < 12; i++) {
      days.push({
        day: i + 2,
        title: activities[i].t,
        desc: activities[i].d,
        iconName: activities[i].i
      });
    }

    // Day 14: 离开
    days.push({
      day: 14,
      title: '打包回家！',
      desc: DEPARTURES[style](data.city),
      iconName: 'Plane'
    });

    result[key] = days;
  }
}

